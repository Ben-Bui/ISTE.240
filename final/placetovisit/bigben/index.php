        <h1>History of The House of Parliament and Big Ben</h1>
        <p> The construction of the Palace of Westminster, home to the British Parliament, began 
            in the mid-19th century after a fire destroyed the previous building. The design by architect 
            Sir Charles Barry with the iconic clock tower, housing the "Great Bell" (most known as Big Ben), 
            by Augustus Pugin, became the symbols of British democracy.<br>

            The Palace of Westminster was constructed in the Gothic Revival style, 
            characterized by its ornate, pointed arches, and spires. The design was a hat off to the rich 
            architectural history of the British Isles.<br>
            
            Contrary to popular belief, "Big Ben" refers to the Great Bell inside the clock tower, not the tower 
            itself. The bell was cast in 1856 and weighs over 13 tons. It is known for its resonant chimes.<br>
            
            The Palace of Westminster and the clock tower were officially opened in 1859. Since then, they have stood 
            as iconic symbols of British democracy and government.<br>
            
            In 1987, the Palace of Westminster, including the clock tower housing Big Ben, was designated a UNESCO World 
            Heritage Site, recognizing its architectural and historical significance.
        </p>
